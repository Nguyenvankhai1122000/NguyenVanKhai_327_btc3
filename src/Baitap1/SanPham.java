     public ArrayList<SP> listSP()
    {
        try {
            ArrayList<SP> listSP = new ArrayList<SP>();
            ResultSet rs = bll.listSP();
            while (rs.next()) {
                SP tb = new SP();
                tb.setMaSP(rs.getString("MaSP"));
                tb.setTenSP(rs.getString("TenSP"));
                tb.setTenLoaiSP(rs.getString("TenLoaiSP"));
                listSP.add(tb);
            }
            return listSP;
        } catch (SQLException ex) {
            Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
public void load()
{
        DefaultTableModel model=(DefaultTableModel) jTableSP.getModel();
        model.setRowCount(0);
          for(SP tb:listSP())
          {
              model.addRow(new Object[]{tb.getMaSP(),tb.getTenSP(),tb.getTenLoaiSP()});
          }
}
