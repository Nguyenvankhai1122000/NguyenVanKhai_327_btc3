   public ArrayList<sach> listSach(ResultSet rs)
    {
        try {
            ArrayList<sach> listSP = new ArrayList<sach>();
            while (rs.next()) {
                sach tb = new sach();
                tb.setMaSach(rs.getString("MASACH"));
                tb.setTuSach(rs.getString("TUSACH"));
                tb.setSoTrang(rs.getInt("SOTRANG"));
                tb.setNamXB(rs.getInt("NAMXB"));
                tb.setTuKhoa(rs.getString("TUKHOA"));
                tb.setNXB(rs.getString("NXB"));
                listSP.add(tb);
            }
            return listSP;
        } catch (SQLException ex) {
            Logger.getLogger(BaiTap3.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
public void load(ResultSet rs)
{
        DefaultTableModel model=(DefaultTableModel) jTableSach.getModel();
        model.setRowCount(0);
          for(sach tb:listSach(rs))
          {
              model.addRow(new Object[]{tb.getMaSach(),tb.getTuSach(),tb.getSoTrang(),tb.getNamXB(),tb.getTuKhoa(),tb.getNXB()});
          }
}
